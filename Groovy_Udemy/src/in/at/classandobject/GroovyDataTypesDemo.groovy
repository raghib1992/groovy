package in.at.classandobject

class GroovyDataTypeDemo {

    static void main(String[] args) {

        def bytevariable = 100
        println("byte variable is " + bytevariable)

        def shortvariable = 1000
        println("short variable is " + shortvariable)

        def intvariable = 10000
        println("int variable is " + intvariable)

        def longvariable = 100000000L
        println("long variable is " + longvariable)

        def floatvariable = 20.1f
        println("float variable is " + floatvariable)

        def doublevariable = 60.34
        println("double variable is " + doublevariable)

        def boolvariable = true
        println("boolean variable is " + boolvariable)

        def charvariable = 'a'
        println("char varaible is " + charvariable)


    }
}
